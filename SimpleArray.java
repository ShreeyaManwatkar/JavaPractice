package com.shree;

public class SimpleArray {

	public static void main(String[] args) {
		int[] numbers = {10,20,30,40,50};
		String[] names = {"Alice","Witch","Invoker","Sniper","Mark"};
		for(int i=0;i<=numbers.length;i++) {
			System.out.println(numbers[i] + " " + names[i]);
		}
	}

}
